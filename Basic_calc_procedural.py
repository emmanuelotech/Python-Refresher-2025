num1 = int(input("num1: "));
num2 = int(input("num2: "));
ops = input("ops: ");

if ops == "+":
    print("**ADDITION OPERATION**")
    print(num1+num2);
elif ops == "-":
    print("**SUBRACTION OPERATION**")
    print(num1-num2);

elif ops == "/":
    print("**DIVISION OPERATION**")
    print(num1/num2);

